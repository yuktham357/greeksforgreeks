class Solution {
  public:
    // Function to count the number of digits in n that evenly divide n
    int evenlyDivides(int n) {
        int count=0;
        int r=n;
        
        while(n>0){
            int rem=n%10;
            if(rem!=0&&r%rem==0){
                count++;
          
            }
            n=n/10;
        }
        return count;
        // code here
    }
};

